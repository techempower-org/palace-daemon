#!/usr/bin/env python3
"""Freeze candidate pools for the rerank eval (issue #46).

Pulls one candidate pool per labeled query from the deployed daemon
(read-only ``GET /search``) and writes them to a JSON file in the shape
``rerank_eval.py --mode candidates`` expects: ``{"candidates": {id: [hit]}}``.

This decouples candidate *retrieval* (needs the live palace, contended) from
candidate *reranking* (deterministic, in-process). Once frozen, the eval can
be re-run offline against the production rerank.py without touching the palace
again — handy when the daemon host is overloaded.

Usage::

    venv/bin/python scripts/evals/collect_candidates.py \
        --url http://familiar:8085 --pool 20 \
        --out docs/evals/rerank-candidates-2026-05-27.json
"""
from __future__ import annotations

import argparse
import json
import os
import sys
import time
from pathlib import Path

import requests

_HERE = Path(__file__).resolve().parent
DEFAULT_QUERIES = _HERE / "rerank_eval_queries.json"


def _is_retryable_http(e: requests.HTTPError) -> bool:
    """True for transient 5xx server errors; False for permanent 4xx."""
    resp = getattr(e, "response", None)
    code = getattr(resp, "status_code", None)
    return isinstance(code, int) and 500 <= code < 600


def _load_env_file(path: Path) -> dict[str, str]:
    out: dict[str, str] = {}
    if not path.exists():
        return out
    for line in path.read_text().splitlines():
        line = line.strip()
        if line and not line.startswith("#") and "=" in line:
            k, _, v = line.partition("=")
            out[k.strip()] = v.strip().strip('"').strip("'")
    return out


def main() -> int:
    ap = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    ap.add_argument("--url", default=None)
    ap.add_argument("--api-key", default=None)
    ap.add_argument("--queries", default=str(DEFAULT_QUERIES))
    ap.add_argument("--pool", type=int, default=20)
    ap.add_argument("--timeout", type=float, default=25.0)
    ap.add_argument("--retries", type=int, default=3)
    ap.add_argument("--delay", type=float, default=0.5)
    ap.add_argument("--out", required=True)
    args = ap.parse_args()

    env = _load_env_file(Path.home() / ".config" / "palace-daemon" / "env")
    url = (args.url or os.getenv("PALACE_DAEMON_URL") or env.get("PALACE_DAEMON_URL") or "http://familiar:8085").rstrip("/")
    api_key = args.api_key or os.getenv("PALACE_API_KEY") or env.get("PALACE_API_KEY")
    if not api_key:
        print("ERROR: no API key", file=sys.stderr)
        return 2

    queries = json.loads(Path(args.queries).read_text())["queries"]
    out: dict[str, list[dict]] = {}
    errors: dict[str, str] = {}

    for i, q in enumerate(queries):
        if i and args.delay:
            time.sleep(args.delay)
        last = None
        for attempt in range(args.retries + 1):
            try:
                r = requests.get(
                    f"{url}/search",
                    params={"q": q["query"], "limit": args.pool},
                    headers={"X-API-Key": api_key},
                    timeout=args.timeout,
                )
                r.raise_for_status()
                hits = r.json().get("results") or []
                # Strip the daemon's own rerank_score so the frozen pool is a
                # clean pre-rerank candidate set — the in-process rerank adds
                # its own score when the eval runs.
                for h in hits:
                    h.pop("rerank_score", None)
                out[q["id"]] = hits
                print(f"ok   {q['id']:<28} n={len(hits)}")
                break
            except (requests.Timeout, requests.ConnectionError) as e:
                last = e
                if attempt < args.retries:
                    time.sleep(3.0 * (attempt + 1))
            except requests.HTTPError as e:
                # 5xx is transient (daemon restart settling) — retry. A 4xx
                # (bad key/query) is permanent; record it and stop retrying
                # this query rather than burning the remaining attempts.
                last = e
                if _is_retryable_http(e) and attempt < args.retries:
                    time.sleep(3.0 * (attempt + 1))
                else:
                    errors[q["id"]] = f"{type(last).__name__}: {last}"
                    print(f"FAIL {q['id']:<28} {errors[q['id']]}", file=sys.stderr)
                    break
        else:
            errors[q["id"]] = f"{type(last).__name__}: {last}"
            print(f"FAIL {q['id']:<28} {errors[q['id']]}", file=sys.stderr)

    payload = {
        "_about": "Frozen candidate pools for the rerank eval (issue #46). Generated by collect_candidates.py against the production palace; feed to rerank_eval.py --mode candidates.",
        "_meta": {
            "url": url, "pool": args.pool,
            "generated_at": time.strftime("%Y-%m-%dT%H:%M:%S%z"),
            "n_queries": len(queries), "n_collected": len(out), "errors": errors,
        },
        "candidates": out,
    }
    outp = Path(args.out)
    outp.parent.mkdir(parents=True, exist_ok=True)
    outp.write_text(json.dumps(payload, indent=2))
    print(f"\nwrote {outp}  ({len(out)}/{len(queries)} pools)")
    return 0 if not errors else 1


if __name__ == "__main__":
    raise SystemExit(main())
